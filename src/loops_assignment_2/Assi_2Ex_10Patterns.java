package loops_assignment_2;

	//10) Write programs to generate following outputs-
public class Assi_2Ex_10Patterns {

	public static void main(String[] args) 
	{
		System.out.println("Pattern -----------------(b)");
		
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(j);
			}
			System.out.println();
		}
		
		
		
		System.out.println("Pattern -----------------(d)");
		
		for(int i=1;i<=4;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(j+" ");
			}
			System.out.println();
		}
		
		
	}

}
