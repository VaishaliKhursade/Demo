package loops;

public class WhileLoop {

	public static void main(String[] args) 
	{
		int i=1;          //start point........
		
		while(i<=10)      //stop point
		{
			System.out.println(i);    //1,2.......
		    
			i++;                      //steps ....i=i+1......i=1+1, i=2+1(increment  ..Decrement)
		    
		}
		

	}

}
